const producto = {
  nombre: "monitor 20 pulgadas",
  precio: 300,
  disponible: true,
};

console.log(Object.keys(producto)); //retorna un arreglo con los keys del objeto
console.log(Object.values(producto)); //retorna un arreglo con llos valores del objeto

console.log(Object.entries(producto)); //retorna un arreglo con los las llaves del objeto y sus valores
